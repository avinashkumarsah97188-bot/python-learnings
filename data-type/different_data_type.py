# Program to create variables of different data types
# and display their values and types



# Integer
age = 23

# Float
height = 5.8

# String
name = "Avinash"

# Boolean
is_student = True

# Complex Number
complex_num = 4 + 3j

# List
subjects = ["Python", "C", "AI"]

# Tuple
marks = (66, 69, 96)

# Dictionary
student = {"Name": "Avinash", "Age": 23}

# Set
colors = {"Red", "Green", "Blue"}

# Display values and their data types
print("Value:", age, "\tType:", type(age))
print("Value:", height, "\tType:", type(height))
print("Value:", name, "\tType:", type(name))
print("Value:", is_student, "\tType:", type(is_student))
print("Value:", complex_num, "\tType:", type(complex_num))
print("Value:", subjects, "\tType:", type(subjects))
print("Value:", marks, "\tType:", type(marks))
print("Value:", student, "\tType:", type(student))
print("Value:", colors, "\tType:", type(colors))
